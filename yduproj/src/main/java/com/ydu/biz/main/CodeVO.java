package com.ydu.biz.main;

public class CodeVO {

	private int codeNo;
	private String bicCodename;
	private String bicCode;
	private String smallCode;
	private String smallCodename;
	
	public int getCodeNo() {
		return codeNo;
	}
	public void setCodeNo(int codeNo) {
		this.codeNo = codeNo;
	}
	public String getBicCodename() {
		return bicCodename;
	}
	public void setBicCodename(String bicCodename) {
		this.bicCodename = bicCodename;
	}
	public String getBicCode() {
		return bicCode;
	}
	public void setBicCode(String bicCode) {
		this.bicCode = bicCode;
	}
	public String getSmallCode() {
		return smallCode;
	}
	public void setSmallCode(String smallCode) {
		this.smallCode = smallCode;
	}
	public String getSmallCodename() {
		return smallCodename;
	}
	public void setSmallCodename(String smallCodename) {
		this.smallCodename = smallCodename;
	}
	
	
}
